"""
FSM состояния для Telegram-бота
"""
from aiogram.fsm.state import State, StatesGroup


# =============================================================================
# Состояния менеджера
# =============================================================================

class ManagerStates(StatesGroup):
    """Состояния менеджера"""
    
    # Регистрация
    REGISTRATION_NAME = State()  # Ожидание имени
    
    # Получение лидов
    LEADS_SEGMENT = State()  # Выбор сегмента
    LEADS_CITY = State()  # Выбор города
    LEADS_COUNT = State()  # Ввод количества
    LEADS_CONFIRM = State()  # Подтверждение


# =============================================================================
# Состояния админа
# =============================================================================

class AdminStates(StatesGroup):
    """Состояния админа"""
    
    # Импорт CSV
    IMPORT_FILE_SELECT = State()  # Выбор файла
    IMPORT_UPLOAD = State()  # Загрузка нового файла
    
    # Проверка дублей
    DUPLICATE_CHECK = State()  # Запуск проверки
    
    # Управление сегментами
    SEGMENT_SELECT = State()  # Выбор сегмента
    SEGMENT_ACTION = State()  # Действие с сегментом
    
    # Очистка данных
    CLEANUP_CONFIRM = State()  # Подтверждение очистки
    
    # Заявки менеджеров
    PENDING_USER_VIEW = State()  # Просмотр заявки
    PENDING_USER_ACTION = State()  # Действие с заявкой
    APPROVE_USER_BITRIX_ID = State()  # Ввод Bitrix24 ID для подтверждения


# =============================================================================
# Общие состояния
# =============================================================================

class CommonStates(StatesGroup):
    """Общие состояния"""

    WAITING = State()  # Ожидание (универсальное)


# =============================================================================
# Состояния обратной связи (Tickets)
# =============================================================================

class FeedbackStates(StatesGroup):
    """Состояния для обратной связи менеджеров"""

    WAITING_FOR_MESSAGE = State()  # Ожидание текста сообщения от менеджера
    WAITING_FOR_ADMIN_RESPONSE = State()  # Ожидание ответа админа
    WAITING_FOR_TICKET_ID = State()  # Ожидание ID тикета для работы


# =============================================================================
# Состояния загрузки лидов админом
# =============================================================================

class AdminLoadLeadsStates(StatesGroup):
    """Состояния для загрузки лидов админом"""
    
    SELECT_MANAGER = State()      # Выбор менеджера
    SELECT_SEGMENT = State()      # Выбор сегментов
    SELECT_CITY = State()         # Выбор города
    ENTER_COUNT = State()         # Ввод количества
    CONFIRM = State()             # Подтверждение


# =============================================================================
# Состояния загрузки на Bitrix24 ID
# =============================================================================

class AdminLoadLeadsBitrixStates(StatesGroup):
    """Состояния для загрузки лидов на Bitrix24 ID"""
    
    ENTER_BITRIX_ID = State()     # Ввод Bitrix24 ID
    SELECT_SEGMENT = State()      # Выбор сегментов
    SELECT_CITY = State()         # Выбор города
    ENTER_COUNT = State()         # Ввод количества
    CONFIRM = State()             # Подтверждение
