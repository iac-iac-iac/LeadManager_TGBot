"""
Emoji для отображения сегментов в кнопках.
"""

def get_segment_emoji(segment: str) -> str:
    """
    Возвращает emoji для сегмента
    
    Args:
        segment: Название сегмента
        
    Returns:
        Emoji
    """
    emojis = {
        "Автосалон": "🚗",
        "Автосервис": "🔧",
        "Автомойка": "🚿",
        "Выкуп автомобилей": "💰",
        "Грузовые автомобили": "🚛",
        "Строительство": "🏗",
        "Производство": "🏭",
        "Розница": "🛒",
        "Услуги": "💼",
        "Нефть": "🛢",
        "Авто": "🚗",
    }
    
    # Ищем частичное совпадение
    for key, emoji in emojis.items():
        if key.lower() in segment.lower():
            return emoji
    
    return "📦"  # По умолчанию
